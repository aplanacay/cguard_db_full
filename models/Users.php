            [['password'], 'string', 'max' => 150],
            [['suffix'], 'string', 'max' => 15],
            [['phone_number'], 'string', 'max' => 30],
            [['type'], 'string', 'max' => 10]
        ];
    }

    /**
     * @inheritdoc
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'username' => 'Username',
            'password' => 'Password',
            'first_name' => 'First Name',
            'middle_name' => 'Middle Name',
            'last_name' => 'Last Name',
